const repo = process.env.GITHUB_REPOSITORY
const token = process.env.GITHUB_TOKEN

if (!repo) throw new Error('GITHUB_REPOSITORY is required')
if (!token) throw new Error('GITHUB_TOKEN is required')

const reportTimeZone = process.env.WEEKLY_TIME_ZONE || 'Pacific/Auckland'
const today = new Date()
const date = formatDateInTimeZone(today, reportTimeZone)
const apiBase = `https://api.github.com/repos/${repo}`
const headers = {
  Accept: 'application/vnd.github+json',
  Authorization: `Bearer ${token}`,
  'X-GitHub-Api-Version': '2022-11-28',
}

async function request(path, options = {}) {
  const response = await fetch(`${apiBase}${path}`, {
    ...options,
    headers: {
      ...headers,
      ...(options.headers || {}),
    },
  })
  if (!response.ok) {
    const body = await response.text()
    throw new Error(`${options.method || 'GET'} ${path} failed: ${response.status} ${body}`)
  }
  return response.json()
}

async function ensureLabel(name, color, description) {
  const encoded = encodeURIComponent(name)
  const response = await fetch(`${apiBase}/labels/${encoded}`, { headers })
  if (response.status === 200) return
  if (response.status !== 404) {
    throw new Error(`label lookup failed for ${name}: ${response.status} ${await response.text()}`)
  }
  await request('/labels', {
    method: 'POST',
    body: JSON.stringify({ name, color, description }),
  })
}

function formatDateInTimeZone(value, timeZone) {
  const parts = new Intl.DateTimeFormat('en-NZ', {
    timeZone,
    year: 'numeric',
    month: '2-digit',
    day: '2-digit',
  }).formatToParts(value)
  const lookup = Object.fromEntries(parts.map((part) => [part.type, part.value]))
  return `${lookup.year}-${lookup.month}-${lookup.day}`
}

const labels = [
  ['task', '0ea5e9', 'Shared Yaomin onboarding task'],
  ['weekly-update', 'fbbf24', 'Weekly progress report'],
  ['status:todo', 'e4e7ec', 'Task not started'],
  ['status:in-progress', '7dd3fc', 'Task in progress'],
  ['status:in-review', 'fbbf24', 'Task in review'],
  ['status:done', '16a34a', 'Task complete'],
  ['priority:high', 'dc2626', 'High priority'],
  ['priority:medium', 'f59e0b', 'Medium priority'],
  ['priority:low', '16a34a', 'Low priority'],
  ['area:onboarding', '7dd3fc', 'Onboarding task'],
  ['area:product', '0ea5e9', 'Product task'],
  ['area:engineering', '6366f1', 'Engineering task'],
  ['area:ai-architecture', '8b5cf6', 'AI architecture task'],
  ['area:design', 'ec4899', 'Design task'],
  ['area:operations', '64748b', 'Operations task'],
]

for (const [name, color, description] of labels) {
  await ensureLabel(name, color, description)
}

const issues = await request('/issues?state=all&per_page=100')
const taskIssues = issues.filter((issue) => !issue.pull_request && issue.labels?.some((label) => label.name === 'task'))
const openTasks = taskIssues.filter((issue) => issue.state === 'open')
const closedTasks = taskIssues.filter((issue) => issue.state === 'closed')
const inReviewTasks = openTasks.filter((issue) => issue.labels?.some((label) => label.name === 'status:in-review'))
const inProgressTasks = openTasks.filter((issue) => issue.labels?.some((label) => label.name === 'status:in-progress'))
const weeklyTitle = `Weekly Progress Update - ${date}`
const existing = issues.find((issue) => !issue.pull_request && issue.title === weeklyTitle)

const issueList = (items) => {
  if (items.length === 0) return '- None'
  return items.slice(0, 10).map((issue) => `- #${issue.number} ${issue.title}`).join('\n')
}

const body = `# Weekly Progress Update - ${date}

Generated by the Yaomin-YNova weekly update workflow for ${reportTimeZone}.

## Summary

- Open shared tasks: ${openTasks.length}
- In progress: ${inProgressTasks.length}
- In review: ${inReviewTasks.length}
- Completed shared tasks: ${closedTasks.length}

## Completed

${issueList(closedTasks)}

## In Progress

${issueList(inProgressTasks)}

## In Review

${issueList(inReviewTasks)}

## Blockers

- Add blockers as comments on this issue.

## Decisions Needed From Tamil

- Add decisions needed as comments on this issue.

## Next Week Focus

${issueList(openTasks)}
`

if (existing) {
  await request(`/issues/${existing.number}`, {
    method: 'PATCH',
    body: JSON.stringify({ body, labels: ['weekly-update'] }),
  })
  console.log(`Updated weekly issue #${existing.number}`)
} else {
  const created = await request('/issues', {
    method: 'POST',
    body: JSON.stringify({
      title: weeklyTitle,
      body,
      labels: ['weekly-update'],
    }),
  })
  console.log(`Created weekly issue #${created.number}`)
}
